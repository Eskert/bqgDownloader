public class Chapter {
    String name;
    String href;

    @Override
    public String toString() {
        return name + "  " + href;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHref() {
        return href;
    }

    public void setHref(String href) {
        this.href = href;
    }
}
